public class Lab65_For_If_LOOP {
    public static void main(String[] args) {

        for (int shruti = 0; shruti <18; shruti++ ){
            if ( shruti > 15) {
                System.out.println("Gift from parent, phone");
            }
            else {
                System.out.println("No gift");
            }
        }
    }
}
