package JAVA_TEST.T1_Java_Programming_Assessment;

public class Q1_Variable_Declaration_Initialization {
    public static void main(String[] args) {
        //Write a Java program that demonstrates proper variable declaration and initialization for different data types.

        // Declaration and initialization of variables

        int age = 25;                 // int type
        double price = 12.14;        // double type
        boolean isValid = true;     // boolean type
        char group = 'A';             // char type

        // Printing the values
        System.out.println("Integer: " + age);
        System.out.println("Double: " + price);
        System.out.println("Boolean: " + isValid);
        System.out.println("Character: " + group);
    }
}
